    INT = auto()
    FLOAT = auto()
    STRING = auto()
    TEXT = auto()
    VECTOR = auto()
    BOOLEAN = auto()
    DATE = auto()
    DATETIME = auto()
    TIME = auto()
    TIMESTAMP = auto()
    JSON = auto()
    JSONB = auto()
    URL = auto()
